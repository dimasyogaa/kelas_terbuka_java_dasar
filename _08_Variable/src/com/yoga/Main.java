package com.yoga;

public class Main {

    public static void main(String[] args) {

        //ini adalah komen inline
        /*
        komen multiline
         */

        // variabel = nilainya dapat berubah ubah
        // x -> identifier (namanya tidak boleh sama dengan nama yang udah ada di pemrograman java)
        int x = 10; // assignment sudah ditentukan nilainya
        System.out.println("nilai x = " + x);

        x = 20;
        System.out.println("nilai x menjadi = " + x);

        //deklarasi : belum ditentukan nilainya memerlukan nilai untuk eksekusi output
        int y;
        y = 7;
        System.out.println("nilai y = " + y);

    }
}
